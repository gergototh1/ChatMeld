interface AgentState {
  presetAgents: Agent[];
  customAgents: Agent[];
  fetchCustomAgents: () => Promise<void>;
  addCustomAgent: (agent: Omit<Agent, 'id'>) => Promise<void>;
  deleteCustomAgent: (id: number) => Promise<void>;
  updateAgent: (id: number, updates: Partial<Agent>) => Promise<void>;
}

export const useAgentStore = create<AgentState>()((set) => ({
  presetAgents,
  customAgents: [],
  fetchCustomAgents: async () => {
    const customAgents = await db.agents.toArray();
    set({ customAgents });
  },
  addCustomAgent: async (agent) => {
    const id = await db.agents.add(agent as Agent);
    set((state) => ({
      customAgents: [...state.customAgents, { ...agent, id }],
    }));
  },
  deleteCustomAgent: async (id) => {
    await db.agents.delete(id);
    set((state) => ({
      customAgents: state.customAgents.filter((a) => a.id !== id),
    }));
  },
  updateAgent: async (id, updates) => {
    await db.agents.update(id, updates);
    set((state) => {
      const updatedCustomAgents = state.customAgents.map((agent) =>
        agent.id === id ? { ...agent, ...updates } : agent,
      );
      const updatedPresetAgents = state.presetAgents.map((agent) =>
        agent.id === id ? { ...agent, ...updates } : agent,
      );

      // Update the active conversation in chatStore if this agent is part of it
      const { activeConversation, updateActiveConversationAgentSettings } = useChatStore.getState();
      if (activeConversation && activeConversation.agents.some(a => a.id === id)) {
        updateActiveConversationAgentSettings(id, updates);
      }

      return {
        customAgents: updatedCustomAgents,
        presetAgents: updatedPresetAgents,
      };
    });
  },
}));
